	<p><font size="4"><b>
		<dd>
		The 2000-year old fort has been the site of many battles, with the Battle of Sinhagad (1670) being the most notable of all. The fort was named ‘Lion Fort’ (Sinhagad) to honor the sacrifice of Tanaji Malusare (right hand of Chhatrapati Shivaji) who lost his life while fighting the Mughals in the battle. The fort, built on the top of a hill, offers an amazing view of the lush greenery surrounding it. Pune Darwaza and Kalyan Darwaza are the two entrances to the fort, with the former being the main entry point for visitors. There are a few temples and a memorial built for Tanaji Malusare inside the fort. Click here to know places to visit with 100km<br>

       Although there is a motorable road till the entrance, trekking to it is preferred by many youngsters. 
       The 13-km trek has a medium difficulty level which makes it one of the best forts near Pune for trekking.<br>

       Distance from Pune: Around 30 km via Sinhagad Ghat Road and Sinhagad Road
