    <p><font size="4"><b>
		<dd>
			Tikona Fort, meaning triangular fort, derives its name from its pyramidal shape. It is a major hill fort in the Maval region near Lonavala, built during the Silhara dynasty’s rule. Nestled in the lap of the Sahyadri Ranges, this 3500-feet high (altitude) hill fort has its colossal entrance as its major highlight. Besides this, it is also popular for its Trimbakeshwar Mahadev temple, seven water tanks, Satavahana-era caves, and a breathtaking view of Pawna Dam.<br>

           The nearest starting point for the hike to Tikona Fort is the parking area near Tikona Peth, the base village for the excursion. The trek is safe, and it will take you around an hour to reach the entrance of the fort. From here, the journey becomes a lot exciting as the steps to the top are very narrow. It is, therefore, advisable to avoid climbing the fort in large groups. <br>

Distance from Pune: Around 51 km via Paud Road
