	<label><font size="6" ><p align="center"><mark>2. Purandar Fort, Pune</mark></font></label><br><br>

    <img src="https://www.fabhotels.com/blog/wp-content/uploads/2018/08/Purandar.jpg" width="650" border="2">
    <p><font size="4"><b>
		<dd>
    Located around 4500 feet above sea level, Purandar Fort was the birthplace of Sambhaji, the son of Chhatrapati Shivaji Maharaj. The fort is of great historical significance as it marks Shivaji’s rise against the Mughals and Bijapur Sultanate. However, Shivaji was not the only ruler of the Purandar Fort, as it was the Yadava dynasty which constructed it in the 11th century. <br>

    An hour-and-half drive amidst the picturesque Western Ghats range will take you to this huge hilltop fort. On reaching the fort, you will notice that the fort has been divided into two distinct levels – the upper level known as Ballekilla and the lower level known as the Machi. Both the upper and lower levels house temples dedicated to Lord Shiva and Purandareshwar, the fort’s patron deity.<br>

    Besides being a popular tourist spot, a part of the fort is used as the training academy of National Cadet Corps. The place is also a hotspot for paragliding and trekking, giving you another reason to come here.<br>

    Distance from Pune: Around 41 km via Saswad-Bopdev-Pune Road
    </dd></b></font></p><hr>


